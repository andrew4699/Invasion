"use strict";

var express = require("express");
var app = express();

var http = require("http").Server(app);
var io = require("socket.io")(http);

global.config = require("../config.json");

app.use(express.static(__dirname + "/../client"));

global.constants = {};

// Includes

require("./includes/functions");

require("./includes/classes/player");

require("./includes/classes/weapon");

// Variables

global.players = [];

// Functions

function onServerStart(port)
{
	clearConsole();

	println("Server started on *:" + port);

	print("[Server has been initailized]");
	println("-----------------------------");
}

function onServerShutdown()
{
	process.exit();
}

// Sockets

io.on("connection", function(socket)
{
	var player = new Player(socket);
	player.onConnect();

	socket.on("disconnect", function()
	{
		player.onDisconnect();
	});

	socket.on("sendHeartbeat", function(account)
	{
		player.onSendHeartbeat();
	});

	socket.on("shoot", function()
	{
		player.onShoot();
	});

	socket.on("reload", function()
	{
		player.onReload();
	});

	socket.on("consoleCommand", function(data)
	{
		player.onConsoleCommand(data.cmd, data.args);
	});
});

